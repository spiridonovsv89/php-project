Путиловская история успешной деятельности завода началась в 1868 году. Пайщики товарищества «Перун» не выполнили условия утверждённого устава, и кредиторы продали завод Н. И. Путилову, что позже было зафиксировано указом Сената от 7 (19) марта 1869 года[15]. Завод начал поставлять рельсы для казны по цене 1 рубль 88 копеек за пуд, положив начало валовому производству рельсов в России. В 1870 году начал работать бессемеровский конвертер, с 1874 года введена в эксплуатацию первая мартеновская печь в Санкт-Петербурге[16].
