    <p>
Основой казённого предприятия явился Кронштадтский чугунолитейный завод, построенный в 1789 году и переведённый в Санкт-Петербург в 1801 году[6]. Этому событию предшествовал указ императора Павла I от 28 февраля 1801 г.[7] Днём рождения завода считается 3 апреля (21 марта по старому стилю) 1801 года, когда на нём было отлито первое пушечное ядро. Завод стал называться Санкт-Петербургским чугунолитейным, входил до января 1827 года в систему Олонецких горных заводов[8], среди первых его руководителей — выходцы из Шотландии: Карл Гаскойн (1801—1806), Адам Армстронг (1807—1818) и Матвей Кларк[9]. В 1806—1807 годах заводом руководил А. М. Полторацкий, по его инициативе завод начал выпускать солдатские ружья[10]. В этот период завод выпускал различные изделия по казённым военным заказам, металлические элементы многих архитектурных ансамблей[11].
</p>
<p>
В результате наводнения 1824 года завод понёс большой ущерб, погибло 152 человека[9]. Завод был переоборудован, на нём было дополнительно организовано котельное производство. После простоя 24 апреля 1842 г.[12] завод был безвозмездно передан Обществу русских горных заводов для изготовления рельсов, их выпуск был начат в 1844 году. В 1847 году завод вернулся в казну, в 1848 году был пожалован генерал-адъютанту Н. А. Огарёву, сначала — во временное безвозмездное владение, а затем — в собственность (1855 г.)[7]. В 1863 году Н. А. Огарёв сдал его в аренду фирме петербургского купца 1-й гильдии Василия Хенлея «Рихтер Дей и К°»[7]. Эта фирма получила в 1858 году казённую ссуду на организацию производства, но не смогла её эффективно использовать и довела завод до банкротства. В марте 1864 года Н. А. Огарёв продал завод кредиторам Василия Хенлея купцу Вильгельму Риттеру и барону Константину Карловичу Фелейзену. С целью оздоровления производства и обеспечения возможности возврата казённого долга завода в сумме около 200 тысяч рублей, 30 сентября (12 октября) 1866 года был высочайше утверждён устав Санкт-Петербургского товарищества железоделательного завода «Перун», которое должно было принять на себя все активы и обязательства завода[13].


